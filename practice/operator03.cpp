#include<iostream>
using namespace std;
int main()
{
    // logical operator 
    int a = 5 , b = 6 ;
    cout<<"the value of ((a==b) && (a<b)) is : "<< ((a==b) && (a<b))<<endl;
    cout<<"the value of ((a==b) or (a<b)) is : "<< ((a==b) or (a<b))<<endl;
    cout<<"the value of (!(a==b)) is : "<< (!(a==b))<<endl;
    
    return 0;



}
